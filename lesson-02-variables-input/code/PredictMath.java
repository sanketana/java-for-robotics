public class PredictMath {
    public static void main(String[] args) {
        int a = 7;
        int b = 2;
        System.out.println(a + b);
        System.out.println(a / b);
        System.out.println(a % b);

        double x = 7.0;
        double y = 2.0;
        System.out.println(x / y);
    }
}
